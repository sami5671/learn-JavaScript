// write a function that will take 3 numbers will return the max number
function maxNumber(n1, n2, n3) {
  const result = Math.max(n1, n2, n3);
  return result;
}

// Main function
var number1 = 5;
var number2 = 6;
var number3 = 7;

const result = maxNumber(number1, number2, number3);
console.log(result);
